# 2- Simple Arithmetic

# Addition
x = 5
y = 3
print(x + y)  # Output: 8

# Subtraction
x = 5
y = 3
print(x - y)  # Output: 2

# Multiplication
x = 5
y = 2
print(x * y)  # Output: 10

# Division
x = 6
y = 3
print(x / y)  # Output: 2.0

# String Operations

# String Concatenation
x = "Hello"
y = "World"
print(x + " " + y)  # Output: "Hello World"

# String Length
x = "Hello"
print(len(x))  # Output: 5

# String Formatting
name = "John"
age = 30
print("My name is {} and I am {} years old.".format(name, age))  # Output: "My name is John and I am 30 years old."

# Conditional Statements

x = 5
if x > 3:
    print("x is greater than 3.")
else:
    print("x is less than or equal to 3.")


# Loops

# For Loop
for i in range(0, 5):
    print(i)

# While Loop
i = 0
while i < 5:
    print(i)
    i += 1

